#include "bits/stdc++.h"
using namespace std;

typedef long long ll;

const int N = 2 * 1e5 + 10;

ll res;
ll a[N];


void solve()
{
	int n,t;
    cin>>n;
    cin>>t;
    int a[n];
    
    int counter=0;
    for(int i=1;i<n;i++)
    {
        cin>>a[i];
    }
    a[n]=1;
    for(int j=1;j<=n;j=j+a[j])
    {
        if(j==t)
        {
            counter++;
            break;
        }
    }
    if(counter>0)
    {
        cout<<"YES";
    }
    else
    {
        cout<<"NO";
    }
}

