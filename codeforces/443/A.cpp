const int N = 2 * 1e5 + 10;

ll res;
ll a[N];


void solve()
{
	string s;
		getline(cin,s);
		set<char> v;
		for (int i = 0; i < s.length(); i++)
		{
			if (isalpha(s[i]))
			{
				v.insert(s[i]);
			}
		}
		cout<<v.size()<<endl;
}

